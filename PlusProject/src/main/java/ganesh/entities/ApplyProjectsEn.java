package ganesh.entities;

public class ApplyProjectsEn {
	
	private int id;
	
	private String projectTitle;
	
	private String projectCompany;
	
	private String projectCompanyEmail;
	
	private String freelancerName;
	
	private String freelancerEmail;
	
	private String freelancerResume;
	
	private String status;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getProjectTitle() {
		return projectTitle;
	}

	public void setProjectTitle(String projectTitle) {
		this.projectTitle = projectTitle;
	}

	public String getProjectCompany() {
		return projectCompany;
	}

	public void setProjectCompany(String projectCompany) {
		this.projectCompany = projectCompany;
	}

	public String getProjectCompanyEmail() {
		return projectCompanyEmail;
	}

	public void setProjectCompanyEmail(String projectCompanyEmail) {
		this.projectCompanyEmail = projectCompanyEmail;
	}

	public String getFreelancerName() {
		return freelancerName;
	}

	public void setFreelancerName(String freelancerName) {
		this.freelancerName = freelancerName;
	}

	public String getFreelancerEmail() {
		return freelancerEmail;
	}

	public void setFreelancerEmail(String freelancerEmail) {
		this.freelancerEmail = freelancerEmail;
	}

	public String getFreelancerResume() {
		return freelancerResume;
	}

	public void setFreelancerResume(String freelancerResume) {
		this.freelancerResume = freelancerResume;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	
	
	
	
}
