package ganesh.entities;

import java.sql.Date;

public class FreelancerEn {
	
	private int id;
	
	private String name;
	
	private String email;
	
	private String phone;
	
	private Date dob;
	
	private String linkedinName;
	
	private String education;
	
	private String profilePhotoF;
	
	private String charges;
	
	private String gender;
	
	private String skills;
	
	private String password;
	
	private String cpassword;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public Date getDob() {
		return dob;
	}

	public void setDob(Date dob) {
		this.dob = dob;
	}

	public String getLinkedinName() {
		return linkedinName;
	}

	public void setLinkedinName(String linkedinName) {
		this.linkedinName = linkedinName;
	}

	public String getEducation() {
		return education;
	}

	public void setEducation(String education) {
		this.education = education;
	}

	public String getProfilePhotoF() {
		return profilePhotoF;
	}

	public void setProfilePhotoF(String profilePhotoF) {
		this.profilePhotoF = profilePhotoF;
	}

	public String getCharges() {
		return charges;
	}

	public void setCharges(String charges) {
		this.charges = charges;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getSkills() {
		return skills;
	}

	public void setSkills(String skills) {
		this.skills = skills;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getCpassword() {
		return cpassword;
	}

	public void setCpassword(String cpassword) {
		this.cpassword = cpassword;
	}
	
	
	
	
}
